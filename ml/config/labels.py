def normalize_label(label: str) -> str:
    if not label:
        return label
    if label.startswith("B-") or label.startswith("I-"):
        prefix, raw = label[:2], label[2:]
        return prefix + CANONICAL_LABELS.get(raw, raw)
    return CANONICAL_LABELS.get(label, label)

def strip_bio(label: str) -> str:
    if label.startswith("B-") or label.startswith("I-"):
        return normalize_label(label)[2:]
    return normalize_label(label)
