def normalize_label(label: str) -> str:
    """
    Normalize BIO-tagged label to final category name.
    
    Strips B- and I- prefixes if present. If label is already normalized,
    returns it unchanged.
    
    Args:
        label: Label string, may be BIO-tagged or final
        
    Returns:
        Final canonical label name
    """
    if not label:
        return label
    
    # Check explicit mapping first
    if label in BIO_TO_FINAL_LABEL:
        return BIO_TO_FINAL_LABEL[label]
    
    # Fallback: strip prefix
    for prefix in ("B-", "I-"):
        if label.startswith(prefix):
            return label[len(prefix):]
    
    return label


def is_bio_label(label: str) -> bool:
    """
    Check if label is BIO-tagged.
    
    Args:
        label: Label string
        
    Returns:
        True if label starts with B- or I-
    """
    return label.startswith("B-") or label.startswith("I-")


def all_final_labels() -> Set[str]:
    """
    Return set of all final canonical label names.
    
    Returns:
        Set of 32 entity category names
    """
    return FINAL_LABELS.copy()
